-- WHY run migrations instead of just executing SQL manually?
--   Migrations are version-controlled, repeatable, and trackable.
--   Every developer runs the same migrations to get an identical DB state.
--   In production, you can see EXACTLY what changed and when.
--
-- Run this with: psql $DATABASE_URL -f migrations/001_create_users.sql
